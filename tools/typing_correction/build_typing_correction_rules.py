OPERATION_NAMES = {
    "transpose": "kAdjacentTranspose",
    "omission": "kMissingKeyInsertion",
    "duplicate": "kDuplicateRemoval",
    "replacement": "kLiteralReplacement",
    "neighbor": "kNeighborSubstitution",
    "kana_modifier": "kKanaModifier",
}
COSTS = {
    "transpose": 100,
    "duplicate": 110,
    "neighbor": 120,
    "omission": 160,
    "replacement": 200,
}
KANA_COSTS = {
    "neighbor": 120,
    # A physical-key neighbor is ambiguous; an unchanged key code with a
    # modifier-sensitive key string is a more specific replay and should win
    # the bounded candidate window.
    "kana_modifier": 100,
}


