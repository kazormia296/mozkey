def _cpp_string(value: str) -> str:
    return json.dumps(value, ensure_ascii=False)


def render_header(root: pathlib.Path = CORPUS_ROOT) -> str:
    corpus = load_corpus(root)
    rules: list[tuple[str, str, str, str, int, bool, bool]] = []
    for case in corpus.gold:
        row = case.row
        rules.append(
            (
                row["case_id"],
                row["typed_raw"],
                row["intended_raw"],
                OPERATION_NAMES[row["operation"]],
                COSTS[row["operation"]],
                row["behavior"] == "auto",
                True,
            )
        )
    for rule in corpus.overrides:
        row = rule.row
        rules.append(
            (
                row["rule_id"],
                row["wrong"],
                row["corrected"],
                OPERATION_NAMES[row["operation"]],
                int(row["cost"]),
                row["auto_applicable"] == "true",
                row["scope"] == "full",
            )
        )

    lines = [
        "// Generated by tools/typing_correction/build_typing_correction_rules.py.",
        "// Do not edit this file directly; update the TSV corpus instead.",
        "",
        "#ifndef MOZC_TYPING_CORRECTION_GENERATED_ROMAN_RULES_H_",
        "#define MOZC_TYPING_CORRECTION_GENERATED_ROMAN_RULES_H_",
        "",
        '#include "typing_correction/roman_typing_corrector.h"',
        "",
        "namespace mozc::typing_correction {",
        "",
        f'inline constexpr char kTypingCorrectionCorpusVersion[] = "{CORPUS_VERSION}";',
        f'inline constexpr char kTypingCorrectionCorpusSha256[] = "{corpus_digest(root)}";',
        f"inline constexpr size_t kTypingCorrectionRomanGoldTargetCount = {EVALUATION_TARGETS['roman_gold']};",
        f"inline constexpr size_t kTypingCorrectionRomanNegativeTargetCount = {EVALUATION_TARGETS['roman_negative']};",
        "",
        "inline constexpr RomanRule kGeneratedRomanRules[] = {",
    ]
    for rule_id, wrong, corrected, operation, cost, auto_applicable, whole in rules:
        lines.append(
            "    {"
            f"{_cpp_string(wrong)}, {_cpp_string(corrected)}, "
            f"Operation::{operation}, {cost}, "
            f"{'true' if auto_applicable else 'false'}, "
            f"{'true' if whole else 'false'}, {_cpp_string(rule_id)}"
            "},"
        )
    lines.extend(
        [
            "};",
            "",
            "inline constexpr RomanGoldCase kGeneratedRomanGoldCases[] = {",
        ]
    )
    for case in corpus.gold:
        row = case.row
        lines.append(
            "    {"
            f"{_cpp_string(row['case_id'])}, {_cpp_string(row['typed_raw'])}, "
            f"{_cpp_string(row['intended_raw'])}, "
            f"{_cpp_string(row['intended_reading'])}, "
            f"Operation::{OPERATION_NAMES[row['operation']]}, "
            f"{'true' if row['behavior'] == 'auto' else 'false'}, "
            f"{_cpp_string(row['stratum'])}"
            "},"
        )
    lines.extend(
        [
            "};",
            "",
            "inline constexpr size_t kGeneratedRomanGoldCaseCount =",
            "    sizeof(kGeneratedRomanGoldCases) /",
            "    sizeof(kGeneratedRomanGoldCases[0]);",
            "",
            "inline constexpr RomanNegativeCase kGeneratedRomanNegativeCases[] = {",
        ]
    )
    for case in corpus.negative:
        row = case.row
        lines.append(
            "    {"
            f"{_cpp_string(row['case_id'])}, {_cpp_string(row['typed_raw'])}, "
            f"{'true' if row['raw_policy'] == 'forbidden' else 'false'}, "
            f"{'true' if row['display_policy'] == 'forbidden' else 'false'}, "
            f"{'true' if row['auto_policy'] == 'forbidden' else 'false'}, "
            f"{_cpp_string(row['stratum'])}"
            "},"
        )
    lines.extend(
        [
            "};",
            "",
            "inline constexpr size_t kGeneratedRomanNegativeCaseCount =",
            "    sizeof(kGeneratedRomanNegativeCases) /",
            "    sizeof(kGeneratedRomanNegativeCases[0]);",
            "",
            "inline constexpr size_t kGeneratedRomanRuleCount =",
            "    sizeof(kGeneratedRomanRules) / sizeof(kGeneratedRomanRules[0]);",
            "",
            "}  // namespace mozc::typing_correction",
            "",
            "#endif  // MOZC_TYPING_CORRECTION_GENERATED_ROMAN_RULES_H_",
            "",
        ]
    )
    return "\n".join(lines)


def _cpp_char(value: str) -> str:
    if len(value) != 1 or ord(value) > 0x7F:
        raise ValueError(f"kana key code must be one ASCII character: {value!r}")
    escaped = value.replace("\\", "\\\\").replace("'", "\\'")
    return f"'{escaped}'"


def render_kana_header(root: pathlib.Path = CORPUS_ROOT) -> str:
    corpus = load_corpus(root)
    if not corpus.kana_gold or not corpus.kana_evaluation or not corpus.kana_negative:
        raise ValueError("kana corpus is required to generate the kana header")

    lines = [
        "// Generated by tools/typing_correction/build_typing_correction_rules.py.",
        "// Do not edit this file directly; update the TSV corpus instead.",
        "",
        "#ifndef MOZC_TYPING_CORRECTION_GENERATED_KANA_RULES_H_",
        "#define MOZC_TYPING_CORRECTION_GENERATED_KANA_RULES_H_",
        "",
        '#include "typing_correction/kana_typing_corrector.h"',
        "",
        "namespace mozc::typing_correction {",
        "",
        f'inline constexpr char kTypingCorrectionKanaCorpusVersion[] = "{CORPUS_VERSION}";',
        f'inline constexpr char kTypingCorrectionKanaCorpusSha256[] = "{corpus_digest(root)}";',
        f"inline constexpr size_t kTypingCorrectionKanaGoldTargetCount = {EVALUATION_TARGETS['kana_gold']};",
        f"inline constexpr size_t kTypingCorrectionKanaGoldFixtureTargetCount = {EVALUATION_TARGETS['kana_gold_fixture']};",
        f"inline constexpr size_t kTypingCorrectionKanaEvaluationTargetCount = {EVALUATION_TARGETS['kana_gold']};",
        f"inline constexpr size_t kTypingCorrectionKanaNegativeTargetCount = {EVALUATION_TARGETS['kana_negative']};",
        "",
        "inline constexpr KanaRule kGeneratedKanaRules[] = {",
    ]
    operation_names = {
        "neighbor": "kNeighborSubstitution",
        "kana_modifier": "kKanaModifier",
    }
    for case in corpus.kana_gold:
        row = case.row
        typed_code = _cpp_char(row["typed_key_codes"])
        corrected_code = _cpp_char(row["corrected_key_codes"])
        lines.append(
            "    {"
            f"{typed_code}, {_cpp_string(row['typed_key_strings'])}, "
            f"{corrected_code}, {_cpp_string(row['corrected_key_strings'])}, "
            f"Operation::{operation_names[row['operation']]}, "
            f"{KANA_COSTS[row['operation']]}, "
            f"{'true' if row['behavior'] == 'auto' else 'false'}, "
            f"{_cpp_string(row['case_id'])}"
            "},"
        )
    lines.extend(
        [
            "};",
            "",
            "inline constexpr KanaGoldCase kGeneratedKanaGoldCases[] = {",
        ]
    )
    for case in corpus.kana_gold:
        row = case.row
        lines.append(
            "    {"
            f"{_cpp_string(row['case_id'])}, "
            f"{_cpp_string(row['typed_key_codes'])}, "
            f"{_cpp_string(row['corrected_key_codes'])}, "
            f"{_cpp_string(row['typed_key_strings'])}, "
            f"{_cpp_string(row['corrected_key_strings'])}, "
            f"Operation::{operation_names[row['operation']]}, "
            f"{'true' if row['behavior'] == 'auto' else 'false'}, "
            f"{_cpp_string(row['stratum'])}"
            "},"
        )
    lines.extend(
        [
            "};",
            "",
            "inline constexpr size_t kGeneratedKanaGoldCaseCount =",
            "    sizeof(kGeneratedKanaGoldCases) / sizeof(kGeneratedKanaGoldCases[0]);",
            "",
            "inline constexpr KanaGoldCase kGeneratedKanaEvaluationCases[] = {",
        ]
    )
    for case in corpus.kana_evaluation:
        row = case.row
        lines.append(
            "    {"
            f"{_cpp_string(row['case_id'])}, "
            f"{_cpp_string(row['typed_key_codes'])}, "
            f"{_cpp_string(row['corrected_key_codes'])}, "
            f"{_cpp_string(row['typed_key_strings'])}, "
            f"{_cpp_string(row['corrected_key_strings'])}, "
            f"Operation::{operation_names[row['operation']]}, "
            f"{'true' if row['behavior'] == 'auto' else 'false'}, "
            f"{_cpp_string(row['stratum'])}"
            "},"
        )
    lines.extend(
        [
            "};",
            "",
            "inline constexpr size_t kGeneratedKanaEvaluationCaseCount =",
            "    sizeof(kGeneratedKanaEvaluationCases) / sizeof(kGeneratedKanaEvaluationCases[0]);",
            "",
            "inline constexpr KanaNegativeCase kGeneratedKanaNegativeCases[] = {",
        ]
    )
    for case in corpus.kana_negative:
        row = case.row
        lines.append(
            "    {"
            f"{_cpp_string(row['case_id'])}, "
            f"{_cpp_string(row['typed_key_codes'])}, "
            f"{_cpp_string(row['typed_key_strings'])}, "
            f"{'true' if row['raw_policy'] == 'forbidden' else 'false'}, "
            f"{'true' if row['display_policy'] == 'forbidden' else 'false'}, "
            f"{'true' if row['auto_policy'] == 'forbidden' else 'false'}, "
            f"{_cpp_string(row['stratum'])}"
            "},"
        )
    lines.extend(
        [
            "};",
            "",
            "inline constexpr size_t kGeneratedKanaNegativeCaseCount =",
            "    sizeof(kGeneratedKanaNegativeCases) / sizeof(kGeneratedKanaNegativeCases[0]);",
            "",
            "inline constexpr size_t kGeneratedKanaRuleCount =",
            "    sizeof(kGeneratedKanaRules) / sizeof(kGeneratedKanaRules[0]);",
            "",
            "}  // namespace mozc::typing_correction",
            "",
            "#endif  // MOZC_TYPING_CORRECTION_GENERATED_KANA_RULES_H_",
            "",
        ]
    )
    return "\n".join(lines)


