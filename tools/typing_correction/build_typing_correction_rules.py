def main(argv: list[str] | None = None) -> int:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--corpus-root", type=pathlib.Path, default=CORPUS_ROOT)
    parser.add_argument("--output", type=pathlib.Path, default=DEFAULT_OUTPUT)
    parser.add_argument(
        "--kana-output", type=pathlib.Path, default=DEFAULT_KANA_OUTPUT
    )
    parser.add_argument(
        "--holdout-output", type=pathlib.Path, default=DEFAULT_HOLDOUT_OUTPUT
    )
    parser.add_argument(
        "--contract-output", type=pathlib.Path, default=DEFAULT_CONTRACT_OUTPUT
    )
    args = parser.parse_args(argv)
    args.contract_output.parent.mkdir(parents=True, exist_ok=True)
    args.contract_output.write_text(
        render_contract_header(), encoding="utf-8", newline="\n"
    )
    output = render_header(args.corpus_root)
    args.output.parent.mkdir(parents=True, exist_ok=True)
    args.output.write_text(output, encoding="utf-8", newline="\n")
    if (args.corpus_root / "kana_gold.tsv").exists():
        kana_output = render_kana_header(args.corpus_root)
        args.kana_output.parent.mkdir(parents=True, exist_ok=True)
        args.kana_output.write_text(kana_output, encoding="utf-8", newline="\n")
        holdout_output = render_holdout_header(args.corpus_root)
        args.holdout_output.parent.mkdir(parents=True, exist_ok=True)
        args.holdout_output.write_text(
            holdout_output, encoding="utf-8", newline="\n"
        )
        print(
            f"generated {args.contract_output}, {args.output}, {args.kana_output}, "
            f"and {args.holdout_output}"
        )
    else:
        print(f"generated {args.contract_output} and {args.output}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
